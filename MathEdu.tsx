// TASK BLOCK — interactive quiz
// ══════════════════════════════════════════════════════════════════════════
const normalize = (s: string) => s.replace(/\s/g, "").toLowerCase();

const TaskBlock: React.FC<{ tasks: Task[] }> = ({ tasks }) => {
  const [answers,    setAnswers   ] = useState<Record<string, string>>({});
  const [results,    setResults   ] = useState<Record<string, "correct" | "wrong">>({});
  const [hintsShown, setHintsShown] = useState<Record<string, boolean>>({});

  const check = (id: string, correct: string) => {
    const ok = normalize(answers[id] ?? "") === normalize(correct);
    setResults(prev => ({ ...prev, [id]: ok ? "correct" : "wrong" }));
  };

  const correctCount = Object.values(results).filter(v => v === "correct").length;

  return (
    <div className="me-task-list">
      {tasks.map((task, i) => (
        <div key={task.id} className="me-task-item">
          <p className="me-task-q">
            <span className="me-task-num">{i + 1}</span>
            {task.question}
          </p>
          <div className="me-task-input-row">
            <input
              className="me-task-input"
              type="text"
              value={answers[task.id] ?? ""}
              onChange={e => setAnswers(prev => ({ ...prev, [task.id]: e.target.value }))}
              onKeyDown={e => { if (e.key === "Enter") check(task.id, task.answer); }}
              placeholder="Жауап..."
              disabled={results[task.id] === "correct"}
            />
            <button
              className="me-task-btn"
              onClick={() => check(task.id, task.answer)}
              disabled={results[task.id] === "correct"}
            >
              Тексеру
            </button>
          </div>
          {results[task.id] === "correct" && (
            <p className="me-task-result me-task-correct">Дұрыс ✓</p>
          )}
          {results[task.id] === "wrong" && (
            <p className="me-task-result me-task-wrong">Қате, қайта көр</p>
          )}
          {task.hint && results[task.id] === "wrong" && !hintsShown[task.id] && (
            <button
              className="me-task-hint-btn"
              onClick={() => setHintsShown(prev => ({ ...prev, [task.id]: true }))}
            >
              Көмек
            </button>
          )}
          {task.hint && hintsShown[task.id] && (
            <div className="me-task-hint">
              <Lightbulb size={15} strokeWidth={2.5} className="me-hint-icon" />
              <span>{task.hint}</span>
            </div>
          )}
        </div>
      ))}
      <p className="me-task-score">{correctCount} / {tasks.length} дұрыс</p>
    </div>
  );
};

