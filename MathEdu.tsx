interface GradeViewProps {
  gradeData:       GradeData;
  progress:        Progress;
  onLessonSelect:  (lesson: Lesson) => void;
  onClearProgress: () => void;
  onBack:          () => void;
}

const GradeView: React.FC<GradeViewProps> = ({ gradeData, progress, onLessonSelect, onClearProgress, onBack }) => {
  const visibleChapters = gradeData.chapters.filter(ch => !ch.isHidden);
  const totalLessons    = visibleChapters.reduce((s, ch) => s + ch.lessons.length, 0);
  const doneTotal       = visibleChapters.reduce((s, ch) => s + ch.lessons.filter(l => progress[l.id]).length, 0);

  let lessonCounter = 0;

  return (
    <main className="me-grade-view me-page">
      <div className="me-container">
        <button className="me-back-btn" onClick={onBack}>← Басты бетке</button>

        <div className="me-grade-header">
          <h1 className="me-grade-title">{gradeData.grade}-сынып тақырыптары</h1>
          <p className="me-grade-sub">
            {visibleChapters.length} бөлім · {totalLessons} тақырып
          </p>
          {doneTotal > 0 && (
            <button className="me-clear-progress" onClick={onClearProgress}>
              Прогресті тазалау
            </button>
          )}
        </div>

        {visibleChapters.map((chapter, ci) => {
          const chapterStart = lessonCounter;
          lessonCounter += chapter.lessons.length;
          const doneLessons = chapter.lessons.filter(l => progress[l.id]).length;
          const pct = chapter.lessons.length > 0
            ? Math.round(doneLessons / chapter.lessons.length * 100)
            : 0;
          return (
            <Reveal key={chapter.title}>
              <div className="me-chapter">
                <div className="me-chapter-head">
                  <div className="me-chapter-num">{ci + 1}</div>
                  <div className="me-chapter-head-info">
                    <h2 className="me-chapter-title">{chapter.title}</h2>
                    {doneLessons > 0 && (
                      <div className="me-chapter-progress">
                        <span className="me-chapter-progress-label">{doneLessons}/{chapter.lessons.length} аяқталды</span>
                        <div className="me-chapter-progress-bar-wrap">
                          <div className="me-chapter-progress-bar" style={{ width: `${pct}%` } as React.CSSProperties} />
                        </div>
                      </div>
                    )}
                  </div>
                </div>
                <div className="me-cards-grid">
                  {chapter.lessons.map((lesson, li) => (
                    <LessonCard
                      key={lesson.id}
                      lesson={lesson}
                      num={chapterStart + li + 1}
                      delay={li * 50}
                      done={progress[lesson.id] === true}
                      onClick={() => onLessonSelect(lesson)}
                    />
                  ))}
                </div>
              </div>
            </Reveal>
          );
        })}
      </div>
    </main>
  );
};

